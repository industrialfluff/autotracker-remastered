RN_PATTERNS = [
    "the (n[0])'s (n[0,1])",
    "(N[0])'s (n[0,1])",
    "(n[0,1]) of (N[0,1])",
    "on the (n[0])'s (n[0,1])",
    "(n[0,1]) of the (n[0,1])",
    "the (a) (n[0,1])",
    "(A) (n[0])",
    "(a) (n[1])",
    "(a) and (a)",
    "(N[0,1]) and (N[0,1])",
    "(v) the (n[0,1])",
    "(v) it all",
    "(v) and (v)",
    "(v) or (v)",
    "the (v) of (a) (n[0,1])",
    "(v) the (a) (n[0])",
    "(v) the (a) (n[1])",
]

def randoname():
    pat = random.choice(RN_PATTERNS)
    while "(" in pat:
        ps, po, pp = pat.partition("(")
        p, pc, pn = pp.partition(")")

        assert pc == ")", "expected ')' in name pattern"

        p = random.choice(p.split("|"))
        if p.startswith("n") or p.startswith("N"):
            idx = random.choice(eval(p[1:]))
            w = random.choice(RN_NOUNS)[idx]
            if idx in [0] and p.startswith("N"):
                if w[0] in "aeiouAEIOU":
                    p = "an " + w
                else:
                    p = "a " + w
            else:
                p = w
        elif p.startswith("a") or p.startswith("A"):
            w = random.choice(RN_ADJECTIVES)
            if p.startswith("A"):
                if w[0] in "aeiouAEIOU":
                    p = "an " + w
                else:
                    p = "a " + w
            else:
                p = w
        elif p.startswith("v") or p.startswith("V"):
            w = random.choice(RN_VERBS)
            p = w
        else:
            raise Exception("invalid name pattern type")

        pat = ps + p + pn

    return pat